async function main() {
  console.log('Creando escorts de ejemplo...')

  for (let i = 0; i < ESCORTS.length; i++) {
    const e = ESCORTS[i] as any
    const email = `escort${i + 1}@diamantes.vip`

    // Skip if user exists
    const existing = await prisma.user.findUnique({ where: { email } })
    if (existing) {
      console.log(`  Skip: ${e.alias} (ya existe)`)
      continue
    }

    const hashedPassword = await hash('demo1234', 10)

    const user = await prisma.user.create({
      data: {
        email,
        password: hashedPassword,
        role: 'escort',
      },
    })

    await prisma.escort.create({
      data: {
        userId: user.id,
        name: e.name,
        alias: e.alias,
        age: e.age,
        city: e.city,
        description: e.description,
        services: e.services,
        phone: e.phone,
        whatsapp: e.whatsapp,
        featured: e.featured,
        active: true,
        mainPhoto: e.mainPhoto,
        nationality: e.nationality,
        height: e.height,
        weight: e.weight,
        measurements: e.measurements,
        bodyType: e.bodyType,
        hairColor: e.hairColor,
        eyeColor: e.eyeColor,
        bustSize: e.bustSize,
        buttSize: e.buttSize,
        waxing: e.waxing,
        tattoos: e.tattoos,
        piercings: e.piercings,
        languages: e.languages,
        atHome: e.atHome,
        hotels: e.hotels,
        homeService: e.homeService,
        price: e.price,
        availability: e.availability,
        verified: e.verified,
        tier: e.tier,
      },
    })

    console.log(`  Creada: ${e.alias} (${e.tier})`)
  }

  console.log('Listo!')
}

main()
  .catch((e) => { console.error(e); process.exit(1) })
  .finally(() => prisma.$disconnect())
